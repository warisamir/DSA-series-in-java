class Solution {
    static int canReach(int[] a, int n) {
        // code here
      if(a[0]==0)
      return 0;
      int id=n-1;
      for(int i=n-1;i>=0;i--){
          if(a[i]+i>=id)id=i;
      }
        return id==0?1:0;
    }
};