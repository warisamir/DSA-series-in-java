class Solution {
    int removals(int[] arr, int n, int k) {
        // code hereA;
        Arrays.sort(arr);
        int st=0,e=0;
        int maxsiz=0;
        while(e<n){
            int diff=arr[e]-arr[st];
            if(diff>k)st++;
            else
           e++;
                maxsiz=Math.max(maxsiz,e-st);
                
            
        }return n-(maxsiz);
    }
}